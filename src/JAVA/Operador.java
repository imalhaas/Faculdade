package JAVA;

public class Operador {

    public static void main(String[] args) {

        int num1, num2;

        num1 = 6;
        num2 = 6;

        num1 += 2; // esta adicionando 2. A mesma coisa que num1++ mas em vez de 1, seria 2.

        System.out.println(num1);
        System.out.println(++num2);


    }
}
